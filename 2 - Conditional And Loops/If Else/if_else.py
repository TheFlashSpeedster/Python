# Check Positive or Negative Number

n1 = int(input("Enter Number:"))

if n1 > 0:
    print("Number is Positive")
else:
    print("Number is Negative")

# Check Even or Odd Number

n2 = int(input("Enter Number:"))

if n2 % 2 == 0:
    print("Number is Even")
else:
    print("Number is Odd")