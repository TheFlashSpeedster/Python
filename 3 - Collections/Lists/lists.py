# Lists
# Store multiple items in single variable
# Items are in Ordered Index
# Can Store different datatype values
# [] - sq braces used

# Index Starts from 0

list = ["apple", "banana", 45, -23, True, 23.11]

# apple = index 0
# banana = index 1
# True = index 4
# etc...

print(list[4]) # Prints True

# Mutable - Can Update Items

list[0] = "mango"
print(list[0])