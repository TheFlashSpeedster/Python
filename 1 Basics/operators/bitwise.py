# Bitwsie Operators
a = 5
b = 3

# AND & - True If Both True
print("a and b:", a & b)

# OR | - True If Either True
print("a or b:", a | b)

# XOR ^ - False If Both Same Type
print("a xor b:", a ^ b)

# NOT ~ - Reverse Result
print("a not b:", a ^ b)


