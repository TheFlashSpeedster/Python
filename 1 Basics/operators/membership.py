# Membership Operators
# Checks if present in list, tuple, etc

fruits_list = ["appple", "banana", "mango", "orange"]

print("If Banana is Present in Fruits List:", "banana" in fruits_list)
print("If Mango is Present in Fruits List:", "mango" in fruits_list)
print("If Guava is Present in Fruits List:", "guava" in fruits_list)

print("If Guava is NOT Present in Fruits List:", "guava" not in fruits_list)