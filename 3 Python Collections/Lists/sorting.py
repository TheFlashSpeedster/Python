# Sorting a List - INTEGERS

list = [60, 30, 70, 40, 80, 10, 20, 50]
print(list)

# list.sort()
list.sort()
print(list) # Default: Ascending Order

# list.sort(reverse=True)
list.sort(reverse=True)
print(list) # Descending Order

# list.reverse()
list.reverse() # Reverse Order
print(list)

