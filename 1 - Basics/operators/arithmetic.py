# Demo Data
n1 = 5
n2 = 2

# Addition +
print("Sum :", n1 + n2) # 5+2 = 7

# Subtraction -
print("Difference :", n1 - n2) # 5-2 = 3

# Multiplication *
print("Product :", n1 * n2) # 5*2 = 10

# Division /
print("Division :", n1 / n2) # 5/2 = 2.5

# Modulus or Remainder %
print("Remainder :", n1 % n2) # 5/2, remainder = 1

# Exponentiation **
# Returns n1^n2 - Power Raise To
print("Exponentiation :", n1 ** n2) # 5 raise to power 2 = 5^2 = 25

# Floor Division //
# After Division, rounds off quotient to nearest whole no.
print("Floor Division", n1 // n2) # Quotient is 2.5, So Result = 2